    <h2>Tutorial Instalasi Laravel di Windows 10 via Composer :</h2>
    <h3>1. Install PHP dan Composer terlebih dahulu pada PC Anda</h3>
    <h3>2. Buat folder baru pada directory Anda untuk menyimpan aplikasi Laravel</h3>
    <h3>3. Buka terminal PC Anda, lalu masuk pada directory yang sudah Anda buat sebelumnya</h3>
    <h3>4. Lanjut tuliskan perintah 'composer create-project laravel/laravel example-app' pada terminal PC Anda untuk memulai download Laravel</h3>
    <h3>5. Tunggu hingga proses selesai, dan pastikan koneksi internet pada PC Anda stabil</h3>
    <h3>6. Apabila sudah muncul keterangan 'Application key set successfully.' selamat artinya Laravel sudah selesai di download</h3>
